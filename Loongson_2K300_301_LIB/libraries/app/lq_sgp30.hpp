#ifndef __LQ_SGP30_HPP__
#define __LQ_SGP30_HPP__

#include "lq_drv_inc.hpp"

/****************************************************************************************************
 * @brief   SGP30 空气质量传感器类，硬件 I2C 驱动
 * @note    使用 ls_fs_i2c 与传感器通信
 *          - 上电后需要 ~15 秒初始化时间
 *          - 需要 ≥1Hz 周期读取以维持内部基线
 *          - 支持基线保存/恢复，避免长时间预热
 ****************************************************************************************************/

/* SGP30 I2C 地址 */
#define SGP30_I2C_ADDR                  ( 0x58 )

/* SGP30 命令集 */
#define SGP30_CMD_INIT_AIR_QUALITY      ( 0x2003 )  /* 初始化空气质量测量 */
#define SGP30_CMD_MEASURE_AIR_QUALITY   ( 0x2008 )  /* 测量空气质量 (eCO2 + TVOC) */
#define SGP30_CMD_GET_BASELINE          ( 0x2015 )  /* 读取基线值 (用于保存) */
#define SGP30_CMD_SET_BASELINE          ( 0x201E )  /* 设置基线值 (用于恢复) */
#define SGP30_CMD_SET_HUMIDITY          ( 0x2061 )  /* 设置湿度补偿 */
#define SGP30_CMD_MEASURE_RAW           ( 0x2050 )  /* 测量原始信号 */
#define SGP30_CMD_GET_FEATURE_SET       ( 0x202F )  /* 读取特性集版本 */
#define SGP30_CMD_MEASURE_TEST          ( 0x2032 )  /* 自检 */
#define SGP30_CMD_SOFT_RESET            ( 0x0006 )  /* 软复位 (通用调用) */

/* SGP30 初始化参数 */
#define SGP30_INIT_WARMUP_MS            ( 15000 )   /* 上电预热时间 15 秒 */
#define SGP30_INIT_AIR_QUALITY_MS       ( 10000 )   /* init_air_quality 后等待 10 秒 */
#define SGP30_MEASURE_INTERVAL_MS       ( 1000 )    /* 建议测量间隔 1 秒 */

/* 默认总线 */
#define SGP30_DEFAULT_BUS               ( LS_I2C_BUS1 )

/* 基线存储文件 */
#define SGP30_BASELINE_FILE             "/home/root/sgp30_baseline.txt"

class lq_sgp30
{
public:
    /********************************************************************************
     * @brief   构造函数
     * @param   _bus  : I2C 总线路径，默认 /dev/i2c-5
     * @param   _addr : I2C 设备地址，默认 0x58
     ********************************************************************************/
    lq_sgp30(std::string _bus = SGP30_DEFAULT_BUS, uint8_t _addr = SGP30_I2C_ADDR);
    ~lq_sgp30();

public:
    /********************************************************************************
     * @brief   初始化传感器 (上电后必须调用)
     * @note    内部会发送 init_air_quality 命令
     *          调用后需等待约 10~15 秒，此期间 read() 返回值无效
     *          会尝试从文件加载已保存的基线以缩短预热时间
     * @return  成功返回 0
     ********************************************************************************/
    int init(void);

    /********************************************************************************
     * @brief   读取 eCO2 和 TVOC 浓度
     * @param   eco2 : [out] 等效 CO2 浓度 (ppm)，400~60000
     * @param   tvoc : [out] 总挥发性有机物 (ppb)，0~60000
     * @return  成功返回 0，失败返回 -1
     * @note    注意：初次上电后前 15 秒内，返回值固定为 eco2=400, tvoc=0
     *          需要以 ≥1Hz 频率持续调用以维持内部基线
     ********************************************************************************/
    int read(uint16_t *eco2, uint16_t *tvoc);

    /********************************************************************************
     * @brief   读取原始信号值（用于高级校准）
     * @param   ethanol_raw : [out] 乙醇原始信号
     * @param   h2_raw      : [out] H2 原始信号
     * @return  成功返回 0
     ********************************************************************************/
    int read_raw(uint16_t *ethanol_raw, uint16_t *h2_raw);

    /********************************************************************************
     * @brief   设置湿度补偿值
     * @param   temperature : 当前环境温度 (℃)
     * @param   humidity    : 当前相对湿度 (%RH)
     * @return  成功返回 0
     * @note    结合 SHT30 数据可提高 SGP30 精度
     *          需转换为绝对湿度 (g/m³) 后写入传感器
     ********************************************************************************/
    int set_humidity_compensation(float temperature, float humidity);

    /********************************************************************************
     * @brief   保存当前基线到文件
     * @return  成功返回 0
     ********************************************************************************/
    int save_baseline(void);

    /********************************************************************************
     * @brief   从文件加载基线
     * @return  成功返回 0
     ********************************************************************************/
    int load_baseline(void);

    /********************************************************************************
     * @brief   获取传感器初始化时间戳（判断数据是否有效）
     * @return  true = 预热完成, false = 还在预热中
     ********************************************************************************/
    bool is_ready(void);

    /********************************************************************************
     * @brief   运行自检
     * @return  成功返回 0
     ********************************************************************************/
    int self_test(void);

private:
    /********************************************************************************
     * @brief   发送命令并读取响应
     * @param   cmd      : 命令码
     * @param   cmd_ms   : 命令发送后等待时间 (ms)
     * @param   buf      : 响应缓冲区
     * @param   buf_len  : 期望读取字节数
     * @return  成功返回 0
     ********************************************************************************/
    int cmd_read(uint16_t cmd, int cmd_ms, uint8_t *buf, int buf_len);

    /********************************************************************************
     * @brief   CRC-8 校验 (SGP30 使用与 SHT30 相同的 CRC-8 多项式)
     ********************************************************************************/
    static uint8_t crc8(const uint8_t *data, int len);

    /********************************************************************************
     * @brief   计算绝对湿度 (g/m³)
     * @param   temperature : 温度 (℃)
     * @param   humidity    : 相对湿度 (%RH)
     * @return  绝对湿度 (g/m³)
     ********************************************************************************/
    static float calc_absolute_humidity(float temperature, float humidity);

private:
    ls_fs_i2c  *i2c_dev;         // I2C 设备对象
    uint8_t     i2c_addr;        // I2C 从设备地址
    bool        initialized;     // 初始化状态
    time_t      init_time;       // 初始化时间戳
    uint16_t    baseline_eco2;   // 已保存的 eCO2 基线
    uint16_t    baseline_tvoc;   // 已保存的 TVOC 基线
    bool        has_baseline;    // 是否有保存的基线
};

#endif
